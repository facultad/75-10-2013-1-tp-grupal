package ar.uba.fi.tecnicas.dojo;

public class ConversorNumero {
	public int valor;
	public char letra;
	public int valorAnterior;
	public char letraAnterior;
	
	public ConversorNumero(int valor, char letra, int valorAnterior, char letraAnterior){
		this.valor = valor;
		this.letra = letra;
		this.valorAnterior = valorAnterior;
		this.letraAnterior = letraAnterior;
	}
}
